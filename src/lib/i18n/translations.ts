/**
 * Récupère une traduction par clé dotée.
 * Exemple : t('nav.dashboard', 'wo') → 'Taablo bi'
 */
export function t(key: string, locale: Locale = 'fr'): string {
  const parts = key.split('.')
  if (parts.length !== 2) return key

  const [section, field] = parts
  const dict = translations[locale]
  if (!dict) return key

  const sectionDict = dict[section]
  if (!sectionDict) return key

  return sectionDict[field] ?? translations.fr[section]?.[field] ?? key
}

